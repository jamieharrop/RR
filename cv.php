          <p>I'm delighted to admit to not having had a 'proper job' since 1995; the year my first book, 'The Carry On Companion', was commissioned. I subsequently turned my life-long passion for film and television into a career encompassing such best-selling books as the 'Monty Python Encyclopedia', 'Last of the Summer Wine the Finest Vintage', 'The Complete Goodies', 'Sid James - Cockney Rebel' and 'Marty Feldman - The Biography of a Comedy Legend'. These and my other works are included in more detail throughout this website.</p>

          <p>As a consultant, researcher, writer and audio commentary moderator, I have worked on many dvd releases, recording with the likes of Jim Dale, Leslie Phillips, Norman Wisdom and June Whitfield. I have narrated documentaries for several Minder releases and was thrilled to re-live one of my childhood television memories when Fabulous Films dubbed the thirteen 'lost' episodes of 'Monkey!' into English for the first time. I wrote the narration for the behind-the-scenes documentary 'Monkey Nuts' and interviewed voice-over artistes including Andrew Sachs, Miriam Margolyes, David Collings and Burt Kwouk. </p>

          <p>I have also written sleeve notes for several compact disc collections of comedy tunes and songs, worked on interactive dvd quiz releases and acted as consultant for various Carry On franchise releases, including the popular Slowdazzle calendars and a collection of Royal Doulton Character Toby Jugs! I was also the consultant for the official 31st anniversary Slowdazzle Monty Python and the Holy Grail calendar of 2005.</p>

          <p><img src="images/cv-aftercolumbus3.jpg" alt="" class="media-right pull-right" />Inaugurating the popular Carry On events at Pinewood Studios that reunited cast and crew alongside devoted fans of the series, I interviewed writer Norman Hudis, Liz Fraser, Patsy Rowlands and other stars of the series.</p>

          <p>I have also hosted sell-out events at the National Film Theatre, interviewing The Goodies and the League of Gentlemen on stage, as well as attending the Edinburgh Festival and interviewing classic comedy stars like Terry Jones and Melvyn Hayes.</p>

          <p>I was a regular guest at the Cult TV conventions, interviewing such heroes as Ray Galton and Alan Simpson, Jack Douglas, Bill Oddie, Don Estelle, Nicholas Courtney and Colin Baker. I subsequently supported the Telly Nation charity and have worked with the celebrity signing events run by Showmasters and the London Film Convention.</p>

          <p>I was a judge in the Best Comedy category for the British Animation Awards and sit on the committee for the annual Best of British Comedy luncheon at B.A.F.T.A. in aid of The Cinema and Television Benevolent Fund. My passion for the best in British comedy also saw me contribute heavily to the best-selling part work The Classic Carry On Collection, as well as the part-work devoted to the popular situation comedy Dad's Army.</p>

          <p><img src="images/cv-carryon.jpg" alt="" class="media-left pull-left" />I co-wrote the television documentary, What's A Carry On? and the radio special, Thou Art Awful?, a celebration of bawdy British humour through the centuries. More recently I have turned my attention to drama, writing Doctor Who for Big Finish; my star casts including sixth Doctor, Colin Baker, Leslie Phillips, Roy Hudd, Doug Bradley and David Tennant on the eve of his thrilling tenure at the Tenth Doctor.</p>

          <p><img src="images/cv-tennant.jpg" alt="" class="media-right pull-right" />I am a frequent guest on radio, taking part in hundreds of broadcasts and often being assigned a mammoth talkathon by the BBC during promotions for their latest dvd and cd releases. My many television credits include interviews for What's A Carry On?, Top Ten: Comedy Records, Will the Real Basil Fawlty Please Stand Up?, Legends: Hattie Jacques, Legends: Terry-Thomas, What the Pythons Did Next, Richard &amp; Judy, Ball-breakers on the Box, God Save the Queens, Norman Wisdom: His Story, 50 Years of Bad Sex. the BBC News, Carry On Forever, The One Show, When Comedy Goes Horribly Wrong, and Blue Peter. As an 'actor' I have made several appearances in perennial sitcom favourite, Last of the Summer Wine, playing a pub customer in Last Post and Pigeon and a mourner at Compo's funeral in Just a Small Funeral.</p> 

          <p>My first stage play, Jeepers Creepers, about the lives of Lauretta and Marty Feldman, played at the Leicester Square Theatre, London, under the direction of Terry Jones.</p>
