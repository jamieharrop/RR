          <p>A Preview, kindly reproduced from the Winter 2005 issue of  There'll Never be Another!</p>

          <p>'Ere listen. How do you link Max Miller to DR.WHO? Well the first DR.WHO was in 1963, which was the year of Maxies death. Not the best answer, so how about our esteemed president Roy Hudd playing the part of Max in a brand new adventure penned by honorary member, writer, broadcaster, and huge Max fan Robert Ross?</p>

          <p>I recently had the great privilege and pleasure of being present at a recording in a studio in south London where these new stories are brought to life for CD by talented producer/director Gary Russell. In this story ‘Pier Pressure’ Colin Baker (TV doctor number 6, 1984-1986) slips effortlessly back into his role as the doctor in this ripping yarn set in 1936 Brighton. In a plot with drama, comedy and twists a plenty, this is cracking stuff and even if you are not a Dr Who fan the Max connection alone will have you riveted to your seat by the speaker! In particular anyone that attended the joyous occasion of the statue unveiling will find particular references very poignant. As I don’t want to give the plot away I can only say fans of Max, Dr Who and the great city of Brighton are going to love this.</p>

          <p>Roy certainly was on sparkling form and clearly enjoying the experience of playing Max in this different context. Also in the cast is the fine horror actor Doug Bradley probably best known for the Pinhead character in the ‘Hellraiser’ series of films. Roy was also delighted to be working with Maggie Stables who plays the doctor’s assistant. They had worked together before for many years in 'Hiss &amp; Boo Music Hall' but had no idea until they arrived at the studio that they would be reunited again professionally.</p>

          <p>During a break from the recording I chatted to Roy and asked him how he thought Max fits in with the Dr. who format. And hardly pausing for breath he was off!</p>

          <p>"It is so interesting how Robert has used Max in this way with Dr. Who, I think it’s terrific! It really does work very well indeed; it’s so unusual and I hope it might encourage Dr Who fans to find out more about Max Miller!</p>

          <p>When I was approached to do this I was keen on the idea immediately. I do feel any use of Max is a good thing and somehow he can be used in any context, because he was a person painted in such broad colours you could put him anywhere! It worked perfectly in ‘Casting Shadows’ (a radio play Roy did about a fictional meeting between Max with Olivier and Rattigan again set in Brighton), and it works perfectly in ‘Pier Pressure’. Robert has done such a marvellous job with this script, he has got the 30’s atmosphere and the way the characters speak spot on."</p>

          <p>At this moment as I was asking Roy if he had a favourite Dr Who actor Colin Baker walked in and over heard, so I hurriedly rephrased the question to who would be his 2nd favourite!</p>

          <p>"I am a fan of Dr Who and I have to say William Hartnell was my favourite. I loved him in the role because he was so daft and eccentric like a Catweazle sort of character. Of course Hartnell was in the first ever ‘Carry On’ film (Sergeant 1958) and ‘Brighton Rock’, you see Brighton again, it’s a gem of a place! The Max Miller society is doing such good work there. So many other organisations give up, they have a few meetings and that’s it, but you’ve got John Henty and he is the man who sparks it up!" (it’s official if Roy says so!)</p>

          <p>I took the opportunity to ask Colin Baker how it was to work with the national treasure that is our society president to which he replied, "An honour and great fun!" </p>
