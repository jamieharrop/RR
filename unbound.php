              <p>Do you remember growing up in the 1970s? Dick Emery was the most famous comedian on British television. His shows would attract millions of viewers. His outrageous gallery of grotesques were played out in a thousand school playgrounds. "Ooh, you are awful!" we would say. Now, where are his shows? Never repeated. Rarely discussed in all those clip-stuffed nostalgia fests on television. A forgotten hero of comedy.</p>

              <p>What about Larry Semon? You've never even heard of him, have you? In the Hollywood of the 1920s he was one of the kings of comedy. Both Stan Laurel and Oliver Hardy *supported* Larry on film. He directed, produced and starred as the Scarecrow in the original film version of 'The Wizard of Oz'. Dead before he was forty, today he hardly gets a footnote in comedy history.</p>

              <p>How about little Jimmy Clitheroe? The Clitheroe Kid. He was one of the most popular characters on radio and television. He even appeared as General Tom Thumb opposite Burl Ives and Terry-Thomas in 'Jules Verne's Rocket to the Moon'. Really! He was still playing a schoolboy well in to his forties. Jimmy's catchphrase was: "Don't some Mothers have 'em!" Now that sounds familiar! But who gives Jimmy the nod of recognition. No~one.</p>

              <p>Until now…</p>

              <p>In this long over-due and affectionate salute, celebrated comedy historian Robert Ross pays tribute to some of the finest, funniest and most fascinating names in comedy – from both sides of the Atlantic. With the passionate input of such favourite comics as Tim Brooke-Taylor, Hattie Hayridge, Roy Hudd, Michael Palin, Ross Noble, Chris Addison and Bernard Cribbins..., Ross will pay long over-due tribute to legends of humour who, for a variety of reasons, didn't quite reach the heady heights of stardom or, once they had, couldn't cope with the pressures. Whether it be personal demons, changing trends in comedy or a rival act undermining their fame, the ups and downs of the Forgotten Heroes of Comedy will make for fascinating reading.</p>

              <p>Presented in alphabetical order for ease of reference and to give a scattered through the ages feel it will be illustrated throughout with a lavish collection of rare film stills and original theatre posters. The book will be the ultimate talking point in pubs and offices and living rooms across the country. I bet he hasn't included the forgotten member of The Three Stooges. That Shemp Howard who reluctantly gave up a profitable career as Hollywood's busiest character actor to join his brother Moe in the team. Oh. Yes. Yes, he has! So whether it is a favourite from the distant smoke and ale-stained world of the Music Hall like the great George Robey – the man dubbed The Prime Minster of Mirth decades before John Major – or the down-beat poetry of Hovis Presley who dropped disenchanted bombs on the late 1990s, the Forgotten Heroes of Comedy will finally elevate them to the Hall of Fame where they belong. Forgotten, no longer.</p>

              <p>With an introductory piece by that very much remembered and influential comedy hero, Monty Python pioneer Terry Jones, this book will enthral and enlighten the most die-hard of comedy admirers.</p>

